                    <h5>Explanations of Classifications</h5>
                    <p>Below is a detailed explanation of the different levels of plant classifications, starting with "Class".</p>

                    <h5>I. Class</h5>
                    <p>The class divides plants into two major groups: <strong>Dicots</strong> and <strong>Monocots</strong>.</p>
                    <ul>
                        <li><strong>Dicotyledons (Dicots):</strong> These plants have two seed leaves and include most wildflowers, representing about two-thirds of all flowering plants. Dicots typically have "net-veined" leaves, meaning the veins branch out from a central vein.</li>
                        <li><strong>Monocotyledons (Monocots):</strong> Monocots, with one seed leaf, include grasses and simpler plants, making up about one-third of all flowering plants. They are characterized by parallel-veined leaves.</li>
                    </ul>

                    <h5>II. Subclass</h5>
                    <p>A subclass groups related plant families, classified in the order in which their differences from a common ancestor are believed to have developed. The names of subclasses usually end in "idae."</p>

                    <h5>III. Order</h5>
                    <p>Each class is further divided into orders, whose names typically end in "ales."</p>

                    <h5>IV. Family</h5>
                    <p>Each order is divided into families, where members share several botanical features. Family names typically end in "aceae," like the Asteraceae family.</p>

                    <h5>V. Genus</h5>
                    <p>The genus groups plants that share recognizable traits. The genus name is always capitalized, like <em>Rudbeckia</em>, which includes Black-eyed Susan.</p>

                    <h5>VI. Species</h5>
                    <p>The species level defines individual plants, distinguishing them based on traits such as color, leaf shape, or where and by whom they were discovered. The species name follows the genus and is never capitalized, like <em>Rudbeckia hirta</em>.</p>

                    <h5>VII. Variety</h5>
                    <p>Variety is used when a plant differs slightly from a certain species, but not enough to warrant a new species name. It follows the genus and species names with "var" and a specific variety name. Example: <em>Linum grandiflorum var rubrum</em> (Scarlet Flax).</p>
